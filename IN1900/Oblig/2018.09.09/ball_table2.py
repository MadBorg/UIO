

def y(t, v0, g=9.8): #funksjponen for y(t)
    y = v0*t-(1/2)*g*t**2
    return y

#inndata
v0 = 10
n = 10
g = 9.8

interval = [0, 2*v0/g] #start og stopp verdien
dt = abs(interval[0]-interval[1])/n #steg lengde

t = [interval[0] + dt * i for i in range(n+1)] #liste med t verdier, med steglengde dt
y = [y(i, v0) for i in t] #liste med y(t) verdier

print(
'''
.___________________________.
||     t      |     y      ||
||-------------------------||'''
)
for i in zip(t,y):
    print('|| - - - - - - - - - - - - ||')
    print('||{:12.4f}|{:12.4f}||'.format(i[0], i[1]))
print('''||-------------------------||
||     t      |     y      ||
.___________________________.''')



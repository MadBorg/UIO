

def y(t, v0, g=9.8):  # funksjponen for y(t)
    y = v0*t-(1/2)*g*t**2
    return y


v0, n, g = 10, 10, 9.8  # inndata

interval = [0, 2*v0/g]  # start og stopp verdien
dt = abs(interval[0]-interval[1])/n  # seg lengde

t = [interval[0] + dt * i for i in range(n+1)]
y = [y(i, v0) for i in t]
assert len(t) == len(y), 't and y list dont have the same len'


# a
ty1 = [t, y]  # lager liste med to rader og n colonner

print(
    '''
._______________________.
||    t     |    y     ||
||---------------------||'''
)
for i, j in zip(ty1[0], ty1[1]):
    print('|| - - - - - - - - - - ||')
    print('||{:10.2f}|{:10.2f}||'.format(i, j))
print('''||---------------------||
||    t     |    y     ||
._______________________.''')


# b)
print(
    '''
._______________________.
||    t     |    y     ||
||---------------------||'''
)

ty2 = [[i, j] for i, j in zip(t, y)]  # lager liste med n rader og 2 colonner
for i, j in ty2:
    print('|| - - - - - - - - - - ||')
    print('||{:10.2f}|{:10.2f}||'.format(i, j))
print('''||---------------------||
||    t     |    y     ||
._______________________.''')



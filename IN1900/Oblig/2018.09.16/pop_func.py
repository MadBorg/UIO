import numpy as np


def population(t, k, B, C):  # pop funksjonen
    N = B/(1+C*np.exp(-k*t))
    return N


t = range(0, 48+1)  # Liste med t verdiene

k, B, C = 0.2, 50000, 9  # inndata

N = [population(i, k, B, C) for i in t]  # skaffer alle N(t) verdiene og lagrer i en liste

print('''
._____________________.
||    t    |    N    ||
||-------------------||''')
for t_val, N_val in zip(t, N):  # kjoerer gjennom t og N listene for aa printe de korresponderende verdiene
    print('||{:9.2f}|{:9.2f}||'.format(t_val, N_val))


